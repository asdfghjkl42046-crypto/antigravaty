/** 五位法官的碎碎念集合 */
export const JUDGE_MURMURS: Record<JudgePersonality, JudgeMurmurs> = {
  traditionalist: TRADITIONALIST_MURMURS,
  algorithmic: ALGORITHMIC_MURMURS,
  elegant: ELEGANT_MURMURS,
  pragmatic: PRAGMATIC_MURMURS,
  power_broker: POWER_BROKER_MURMURS,
};

/** 碎碎念觸發時機 key（方便程式呼叫） */
export type MurmurTrigger = keyof JudgeMurmurs;

/**
 * 隨機取得一句碎碎念
 * @param personality 法官性格
 * @param trigger 觸發時機
 * @returns 隨機的碎碎念台詞，若找不到則回傳空字串
 */
export function getRandomMurmur(
  personality: JudgePersonality | null,
  trigger: MurmurTrigger
): string {
  if (!personality) return '';
  const murmurs = JUDGE_MURMURS[personality]?.[trigger];
  if (!murmurs || murmurs.length === 0) return '';
  return murmurs[Math.floor(Math.random() * murmurs.length)];
}
